from fpdf import FPDF


def convert_to_list_of_rows(grid):

    return [
        "".join("  " if col == "" else col for col in row) + "\n"
        for row in grid
    ]


grid = [
    ["a", "", "", "", "", "", "", "", "", "", "", "", "", ""],
    ["", "b", "", "", "", "", "", "", "", "", "", "", "", ""],
    ["", "", "c", "", "", "", "", "", "", "", "", "", "", ""],
    ["", "", "", "d", "", "", "", "", "", "", "", "", "", ""],
    ["", "", "", "", "e", "", "", "", "", "", "", "", "", ""],
    ["", "", "", "", "", "f", "", "", "", "", "", "", "", ""],
    ["", "", "", "", "", "", "g", "", "", "", "", "", "", ""],
    ["", "", "", "", "", "", "", "h", "", "", "", "", "", ""],
    ["", "", "", "", "", "", "", "", "i", "", "", "", "", ""],
    ["", "", "", "", "", "", "", "", "", "j", "", "", "", ""],
    ["", "", "", "", "", "", "", "", "", "", "k", "", "", ""],
    ["", "", "", "", "", "", "", "", "", "", "", "l", "", ""],
    ["", "", "", "", "", "", "", "", "", "", "", "", "m", ""],
    ["", "", "", "", "", "", "", "", "", "", "", "", "", "n"],
]

lol = open("temp.txt", "w")

for test in convert_to_list_of_rows(grid):

    lol.write(test)

lol.close()

pdf = FPDF()

pdf.add_page()

pdf.set_font("Arial", size=15)

test = open("temp.txt", "r")

for x in test:
    pdf.cell(200, 10, txt=x, ln=1, align='L')

pdf.output("mygfg.pdf")
